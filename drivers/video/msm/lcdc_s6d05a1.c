void s6d05a1_esd(void)
{
	DPRINT("start %s, panel_initialized: %d,disp_initialized: %d\n", 
      __func__, s6d05a1_state.panel_initialized, s6d05a1_state.disp_initialized);
	if (!s6d05a1_state.panel_initialized) {
      if(  gpio_get_value(GPIO_ESD_DET) == 1 )
      {		
		lcdc_s6d05a1_panel_off(NULL);
		mdelay(20);
		gpio_set_value(lcd_reset, 0); //LCD_RESET_N_LO
		msleep(300); 
		gpio_set_value(lcd_reset, 1);
		msleep(1);
		gpio_set_value(lcd_reset, 0);
		msleep(1);
		gpio_set_value(lcd_reset, 1);
		msleep(10);

		lcdc_s6d05a1_panel_on(NULL);
		}
	  }
      esd_repeat_count = 0;
      del_timer(&g_lcd_esd_timer);
      g_lcd_esd_timer.expires = get_jiffies_64() + msecs_to_jiffies(1000);
      add_timer(&g_lcd_esd_timer);	
}

