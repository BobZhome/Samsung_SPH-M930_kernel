static int lcdc_s6d05a1_panel_on(struct platform_device *pdev)
{
	static int bring_up_condition = 0;
	unsigned size;

	DPRINT("start %s, bring_up %d, disp_initialized %d\n", __func__, bring_up_condition, s6d05a1_state.disp_initialized);

	if(!bring_up_condition) {
		/* trick initalization for timing issue */
		bring_up_condition = 1;

		/* Configure reset GPIO that drives DAC */
		spi_init();	/* LCD needs SPI */
		lcdc_s6d05a1_pdata->panel_config_gpio(1);
		s6d05a1_state.disp_powered_up = TRUE;
		s6d05a1_state.display_on = TRUE;
		s6d05a1_state.disp_initialized = TRUE;
#if 1 /* support different types of LCD */
		samsung_vendor1_id *smem_vendor1_id = (samsung_vendor1_id *)smem_get_entry(SMEM_ID_VENDOR1, &size);
		if(smem_vendor1_id && smem_vendor1_id->lcd_type == LcdTypeDTC) {
			lcd_type = LcdTypeDTC;
		}
		DPRINT("%s, lcd is %s panel!\n", __func__, (lcd_type==LcdTypeDTC)?"DTC":"SMD");
#endif
	}
	else {
		if (!s6d05a1_state.disp_initialized) {
			/* Configure reset GPIO that drives DAC */
			s6d05a1_disp_powerup();
			spi_init();	/* LCD needs SPI */
			lcdc_s6d05a1_pdata->panel_config_gpio(1);
			s6d05a1_disp_on();
			s6d05a1_state.disp_initialized = TRUE;
#ifdef FEATURE_LCD_ESD_DET
			if(system_rev >= 9) {
				if (s6d05a1_state.irq_disabled && USED_ESD_DETET) {
					enable_irq(MSM_GPIO_TO_INT(GPIO_ESD_DET));
					s6d05a1_state.irq_disabled = FALSE;
				}
			}
#endif			
		}
	}

	return 0;
}

static int lcdc_s6d05a1_panel_off(struct platform_device *pdev)
{
	int i;

	DPRINT("start %s\n", __func__);	

	if (s6d05a1_state.disp_powered_up && s6d05a1_state.display_on) {
		#if defined(CONFIG_MACH_VITAL2)
			if(system_rev >= 5) {
				if ((system_rev >= 9) && USED_ESD_DETET) {
					if( !s6d05a1_state.irq_disabled && !phone_power_off) {  
						disable_irq(MSM_GPIO_TO_INT(GPIO_ESD_DET));
						s6d05a1_state.irq_disabled = TRUE;
						}
					}
				for (i = 0; i < POWER_OFF_SEQ_SMD; i++)
					setting_table_write(&power_off_sequence_smd[i]);
				}
			else {
				for (i = 0; i < POWER_OFF_SEQ; i++)
					setting_table_write(&power_off_sequence[i]);
				}
		#else
				for (i = 0; i < POWER_OFF_SEQ; i++)
					setting_table_write(&power_off_sequence[i]);
		#endif
		
		lcdc_s6d05a1_pdata->panel_config_gpio(0);
		s6d05a1_state.display_on = FALSE;
		s6d05a1_state.disp_initialized = FALSE;
		s6d05a1_disp_powerdown();
		}
	return 0;
}

static DEFINE_SPINLOCK(bl_ctrl_lock);


extern void pm8058_wait_pm_lock(void);
void kinetic_tech_backlight_ctrl(int level /* 1 to 32 */)
{
	int i;
	int data;
	int pulse_count;
	unsigned long flags;
	static int accumulated_pulse = 19; // must be exactly same as the brightness set in the bootloader
	/* There are two ways to control backlight level.
	 * 1. Adding the number of pulses to the previously accumulated
	 * 2. Pull low for 3ms or longer to reset the IC
	 * We will use the first method.
	 *
	 * Note : level 1 is the darkest and level 32 is the brightest
	 */
	 
	if(level < 0) level = 0;
	if(level > 32) level = 32;

#if defined(CONFIG_MACH_CHIEF)
	// free up pm lock to prevent scheduling while atomic.
	if(system_rev < 10)
		pm8058_wait_pm_lock();
#else
	// free up pm lock to prevent scheduling while atomic.
	pm8058_wait_pm_lock();
#endif
	// do nothing and lock interrupt as fast as possible!
	spin_lock_irqsave(&bl_ctrl_lock, flags);

	if(level == 0) {
		/* pull low for 3ms to turn off */
		LCD_BL_CTRL(0);
		mdelay(3);
		accumulated_pulse = 0;
		goto cleanup;
	}

	data = 32-level+1;
		
	if(data > accumulated_pulse)
		pulse_count = data - accumulated_pulse;
	else		
		pulse_count = 32 - accumulated_pulse + data;

	if(accumulated_pulse == data)
		goto cleanup;
	
	accumulated_pulse = data;
	
	for(i=0;i<pulse_count;i++) {
		LCD_BL_CTRL(0);
		ndelay(200);
		LCD_BL_CTRL(1);
		ndelay(200);
	}
cleanup:
	spin_unlock_irqrestore(&bl_ctrl_lock, flags);
}

